# string slicing / selecting sub sequences
name = "kaderye"

# positions (index number)
# k = 0 or -7
# a = 1 or -6
# d = 2 or -5
# e = 3 or -4
# r = 4 or -3
# y = 5 or -2
# e = 6 or -1

print(name[2])

# syntax - [start argument : stop argument -1]
print(name[0:2])
print(name[2:5])
print(name[:3])
print(name[2:])
print(name[:])
